/**
 * Card UI Components
 *
 * A collection of card-related components including Card, CardHeader, CardTitle,
 * CardDescription, CardContent, and CardFooter. Provides a consistent card layout
 * system with proper spacing, borders, and theming support. Used throughout
 * the application for content organization and visual hierarchy.
 */

import React from 'react';
import { cn } from '@/lib/utils/cn';

interface CardProps extends React.HTMLAttributes<HTMLDivElement> {
  children: React.ReactNode;
}

const Card = React.forwardRef<HTMLDivElement, CardProps>(
  ({ className, children, ...props }, ref) => {
    return (
      <div
        ref={ref}
        className={cn(
          'rounded-xl border border-slate-200 bg-white shadow-sm transition-all duration-300 hover:shadow-lg hover:bg-slate-50 hover:-translate-y-0.5 dark:border-slate-700 dark:bg-slate-800 dark:hover:bg-slate-700',
          className
        )}
        {...props}
      >
        {children}
      </div>
    );
  }
);

Card.displayName = 'Card';

